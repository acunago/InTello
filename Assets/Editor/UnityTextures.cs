    }

    private void OnEnable()
    {
        darkList = GetDarkLists(darkList);
        lightList = GetLightLists(lightList);
        iconList = GetIconLists(iconList);
        otherList = GetOtherLists(otherList);

        _page = 1;
        _pageTotal = 1;
        _sort = 50;
        _min = 0;
        _max = 50;
    }

    private void OnGUI()
    {
        DrawGUI();

        if (GUI.changed) Repaint();
    }

    private void DrawGUI()
    {
        _selectedList = EditorGUILayout.Popup("Choose a List", _selectedList, _optionsList, EditorStyles.popup);
        EditorGUILayout.Space();

        if (pathList == null)
            pathList = new List<string>();

        switch (_selectedList)
        {
            case 0:
                pathList = darkList;
                break;
            case 1:
                pathList = lightList;
                break;
            case 2:
                pathList = iconList;
                break;
            case 3:
                pathList = otherList;
                break;
        }

        _min = (_page - 1) * _sort;
        _max = _page * _sort;
        _pageTotal = (int)(pathList.Count / _sort) + 1;

        if (_max > pathList.Count)
            _max = pathList.Count;

        EditorGUILayout.BeginHorizontal();
        {
            if (GUILayout.Button("Prev", GUILayout.Width(50)))
            {
                _page--;
                if (_page < 1) _page = 1;
            }
            if (GUILayout.Button("Next", GUILayout.Width(50)))
            {
                _page++;
                if (_page > _pageTotal) _page = _pageTotal;
            }
            EditorGUIUtility.labelWidth = 40;
            _page = EditorGUILayout.IntField("Page:", _page);
            _page = Mathf.Clamp(_page, 0, _pageTotal);
            EditorGUILayout.LabelField("/" + _pageTotal);
            GUILayout.FlexibleSpace();

            EditorGUIUtility.labelWidth = 40;
            _sort = EditorGUILayout.IntField("Sort:", _sort);
        }
        EditorGUILayout.EndHorizontal();
        EditorGUILayout.Space();

        EditorGUILayout.BeginHorizontal();
        {
            EditorGUILayout.LabelField("", GUILayout.Width(50));
            EditorGUILayout.LabelField("Path", EditorStyles.boldLabel, GUILayout.ExpandWidth(true));
            EditorGUILayout.LabelField("Width", EditorStyles.boldLabel, GUILayout.Width(_textureWidth));
            EditorGUILayout.LabelField("Height", EditorStyles.boldLabel, GUILayout.Width(_textureHeight));
        }
        EditorGUILayout.EndHorizontal();

        _scrollPosition = GUILayout.BeginScrollView(_scrollPosition, false, false);
        {
            for (int i = _min; i < _max; i++)
            {
                if (string.IsNullOrEmpty(pathList[i]))
                    continue;

                EditorGUILayout.BeginHorizontal();
                {
                    DrawGUIItem(pathList[i]);
                }
                EditorGUILayout.EndHorizontal();
            }
        }
        GUILayout.EndScrollView();
    }

    void DrawGUIItem(string path)
    {
        Texture tex = EditorGUIUtility.Load(path) as Texture2D;

        if (tex == null)
        {
            EditorGUILayout.LabelField("null:" + path, EditorStyles.miniLabel);
            return;
        }

        _btnCntent.text = "";
        _btnCntent.tooltip = "";
        _btnCntent.image = tex;

        if (GUILayout.Button(_btnCntent, GUILayout.Width(50), GUILayout.MinHeight(20)))
            EditorGUIUtility.systemCopyBuffer = path;

        EditorGUILayout.TextField(path, GUILayout.ExpandWidth(true));
        EditorGUILayout.LabelField(tex.width.ToString(), GUILayout.Width(_textureWidth));
        EditorGUILayout.LabelField(tex.height.ToString(), GUILayout.Width(_textureHeight));
    }

